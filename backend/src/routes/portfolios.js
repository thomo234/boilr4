/**
 *  @swagger
 *  /api/portfolios:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Portfolios]
 *      summary: Get all portfolios
 *      description: Get all portfolios
 *      responses:
 *        200:
 *          description: Portfolios list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Portfolios"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */
router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;

    const globalAccess = req.currentUser.app_role.globalAccess;

    const payload = await PortfoliosDBApi.findAll(req.query, globalAccess);
    if (filetype && filetype === 'csv') {
      const fields = [
        'id',
        'title',
        'description',
        'dimensions',
        'issue_number',
        'price',
      ];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

